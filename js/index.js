var authors = "https://raw.githubusercontent.com/asozialesnetzwerk/zitate/master/namen.txt"
var quotes = "https://raw.githubusercontent.com/asozialesnetzwerk/zitate/master/zitate.txt"

window.arr = [];
var getQuote = function(data) {
    arr.push(data.split(/\n/));
    if(window.arr.length==2) createZitat();
};
$.get(authors, getQuote, 'text');
$.get(quotes, getQuote, 'text');

//createZitat();
$(".get-quote").on("click", function() {
    
	//$.getJSON(api, getQuote, "jsonp");
    createZitat();
});

function createZitat()
{
    var firstAuthor = window.arr[0][Math.floor(Math.random() * window.arr[0].length)];
    var firstQuote = window.arr[1][Math.floor(Math.random() * window.arr[1].length)];
    $(".quote-text").text(firstQuote);
    $(".quote-author").text("- " + firstAuthor);
    
    $(".tweet").attr("href", "https://twitter.com/intent/tweet?text=" + encodeURIComponent(firstQuote + ' - ' + firstAuthor + "\nGenerated by https://github.com/asozialesnetzwerk/zitate"));
}
